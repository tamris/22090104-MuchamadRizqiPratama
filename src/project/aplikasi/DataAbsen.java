     private void TampilData() {
      try {
            st = c.createStatement();
            rs = st.executeQuery("SELECT tbl_hadir.nis, tbl_hadir.nama, tbl_hadir.kelas, tbl_hadir.waktu_hadir, tbl_keluar.waktu_keluar "
                                 + "FROM tbl_hadir LEFT JOIN tbl_keluar ON tbl_hadir.id_kehadiran = tbl_keluar.id_kehadiran");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("NIS");
            model.addColumn("Nama");
            model.addColumn("Kelas");
            model.addColumn("Waktu Hadir");
            model.addColumn("Waktu Keluar");
            
            int no = 1;
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
               Object[] data = {
                   no ++,
                   rs.getString("nis"),
                   rs.getString("nama"),
                   rs.getString("kelas"),
                   rs.getString("waktu_hadir"),
                   rs.getString("waktu_keluar")    
                       
               };
                model.addRow(data);
                tblData.setModel(model);
            }
        } catch (Exception e) {
        }
    }

