        rs = st.executeQuery("SELECT tbl_hadir.nis, tbl_hadir.nama, tbl_hadir.kelas, tbl_hadir.waktu_hadir, tbl_keluar.waktu_keluar FROM tbl_hadir LEFT JOIN tbl_keluar ON tbl_hadir.id_kehadiran = tbl_keluar.id_kehadiran WHERE DATE(tbl_hadir.waktu_hadir) = '" + formattedDate + "'");

